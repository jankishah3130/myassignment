a=int(input("enter the length of parallel side a: "))
b=int(input("enter the length of parallel side b: "))
c=int(input("enter the length of non-parallel side c: "))
d=int(input("enter the length of non-parallel side d: "))
h=int(input("enter the height h: "))
area_of_trapezium=0.5*(a+b)*h
perimeter_of_trapezium=a+b+c+d
print(f'area of trapezium:{area_of_trapezium}')
print(f'perimeter of trapezium:{perimeter_of_trapezium}')
